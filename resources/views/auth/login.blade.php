<script>
    function showPassword() {
        var x = document.getElementById("password");
        var eye = document.getElementById("eyePw");

        if (x.type === "password") {
            x.type = "text";
            eye.className = "bi bi-eye input-group-text"
        } else {
            x.type = "password";
            eye.className = "bi bi-eye-slash input-group-text"
        }
    }
